from turtle import *

t = Turtle()  # создаю черепашку
t.shape('turtle')  # внешний вид - черепашка
t.fd(100)  # идти вперед на 100 шагов
t.lt(50)  # повернуть влево на 50 градусов
t.bk(50)  # идти назад на 50 шагов
t.rt(30)  # повернуть вправо на 30 градусов
t.fd(30)  # идти вперед на 30 градусов

t.circle(70)  # нарисовать окружность с радиусом 70
t.dot(70)  # нарисовать круг с диаметром 70
done()  # чтобы окно не закрывалось сразу



